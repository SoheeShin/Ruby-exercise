



             
def morse_encode(str)
morse_code = {
              "a" => ".-", "b" => "-...", "c" => "-.-.,", "d" => "-..",
              "e" => ".", "f" => "..-.", "g" => "--." , "h" => "....",
              "i" => "..", "j" => ".---", "k" => "-.-", "l" => ".-..",
              "m" => "--", "n" => "-.", "o" => "---", "p" => ".--.",
              "q" => "--.-", "r" => ".-.", "s" => "...", "t" => "-",
              "u" => "..-", "v" => "...-", "w" => ".--", "x" => "-..-",
              "y" => "-.--", "z" => "--..", " " => "  "
              }
  array = str.downcase.split(//)
  array.map! {|x| morse_code[x]}
  array.join(" ")
end

print morse_encode("cat in hat")